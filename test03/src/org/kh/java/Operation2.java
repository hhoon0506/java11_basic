package org.kh.java;

public class Operation2 {

	public static void main(String[] args) {
	// +,-,*,/,%
		int su1 = 98;
		int su2= 18;
		System.out.println("su1 +su2="+(su1 +su2));
		System.out.println("su1 -su2="+(su1 -su2));
		System.out.println("su1 *su2="+(su1 *su2));
		System.out.println("su1 /su2="+(su1 /su2));
		System.out.println("su1 %su2="+(su1 %su2));
	
	}

}
